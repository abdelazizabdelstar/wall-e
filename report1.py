def draw_circle(r=0.5,xc=0,yc=0):
    glBegin(GL_POLYGON)

    for theta in np.arange(0, 2 * pi, .0001):
        x =xc+ r * sin(theta)
        y = yc+ r * cos(theta)
        glVertex(x, y)

    glEnd()

